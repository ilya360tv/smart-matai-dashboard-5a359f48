  id uuid NOT NULL DEFAULT gen_random_uuid() PRIMARY KEY,
  size text NOT NULL,
  direction text NOT NULL,
  type_9016t integer NOT NULL DEFAULT 0,
  type_9001t integer NOT NULL DEFAULT 0,
  type_7126d integer NOT NULL DEFAULT 0,
  type_0096d integer NOT NULL DEFAULT 0,
  type_mr09 integer NOT NULL DEFAULT 0,
  total integer NOT NULL DEFAULT 0,
  created_at timestamp with time zone NOT NULL DEFAULT now(),
  updated_at timestamp with time zone NOT NULL DEFAULT now()
);

-- Enable RLS
ALTER TABLE public.doors_d88 ENABLE ROW LEVEL SECURITY;

-- Create policies
CREATE POLICY "Enable read access for all users" ON public.doors_d88 FOR SELECT USING (true);
CREATE POLICY "Enable insert access for all users" ON public.doors_d88 FOR INSERT WITH CHECK (true);
CREATE POLICY "Enable update access for all users" ON public.doors_d88 FOR UPDATE USING (true);
CREATE POLICY "Enable delete access for all users" ON public.doors_d88 FOR DELETE USING (true);

-- Add trigger for updated_at
CREATE TRIGGER update_doors_d88_updated_at
  BEFORE UPDATE ON public.doors_d88
  FOR EACH ROW
  EXECUTE FUNCTION public.update_updated_at_column();

